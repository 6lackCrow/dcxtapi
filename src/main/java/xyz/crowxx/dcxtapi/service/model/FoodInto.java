package xyz.crowxx.dcxtapi.service.model;

public class FoodInto {

    /**
     * name : 嘻嘻3
     * price : 12
     * num : 2
     */

    private String name;
    private int price;
    private int num;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
